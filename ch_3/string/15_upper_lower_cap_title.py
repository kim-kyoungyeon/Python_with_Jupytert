string = 'this is MY DOG'

# uppper() : 소문자를 대문자로
print(string.upper()) # THIS IS MY DOG

# lower() : 대문자를 소문자로
print(string.lower()) # this is my dog

# capitalize() : 문장의 첫 글자를 대문자로
print(string.capitalize()) # This is my dog

# title() : 단어의 첫 글자를 대문자로 --- 단어 구분 : 공백
print(string.title()) # This Is My Dog






