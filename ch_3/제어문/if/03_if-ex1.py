#다음과 같은 로그인 프로그램 작성
#등록된 id : flower
#비밀번호 1234
#id와 비밀번호 를 입력받아
#로그인 성공 또는 로긍니 실패 출력
# id / 비밀번호 변수에 저장
ID = 'flower'
password = '1234'
#입력코드
input_id = input('아이디입력:')
input_pass = input('비밀번호 입력:')
#비교 후 출력 코드
if(ID==input_id) and (password==input_pass) :
    print('로그인성공')
else :
    print('로그인실패')


















































































































